    <p><br>
    Indian Railways (IR) is a statutory body under the ownership of Ministry of Railways, Government of India that operates India's national railway system.
    It manages the fourth largest national railway system in the world by size, with a total route length of 68,103 km (42,317 mi) as of 31 March 2022.
    52,247 km (32,465 mi) or 83% of all the broad-gauge routes are electrified with 25 kV 50 Hz AC electric traction as of 1 April 2022.
    In 2020, Indian Railways carried 808.6 crore (8.086 billion) passengers and in 2022, Railways transported 1418.1 million tonnes of freight.
    It runs 13,169 passenger trains daily, on both long-distance and suburban routes, covering 7,325 stations across India.
    Mail or Express trains, the most common types of trains, run at an average speed of 50.6 km/h (31.4 mph). Suburban EMUs run at an average speed of 37.5 km/h (23.3 mph).
    Ordinary passenger trains (incl. mixed) run at an average speed of 33.5 km/h (20.8 mph).
    The maximum speed of passenger trains varies, with the Vande Bharat Express running at a peak speed of 180 km/h (110 mph).
    In the freight segment, IR runs 8,479 trains daily.The average speed of freight trains is around 42.2 km/h (26.2 mph). 
    The maximum speed of freight trains varies from 60-75 km/h (37-47 mph) depending on their axle load with 'container special' trains running at a peak speed of 100 km/h (62 mph).
    As of March 2020, Indian Railways' rolling stock consisted of 2,93,077 freight wagons, 76,608 passenger coaches and 12,729 locomotives.
    IR owns locomotive and coach-production facilities at several locations in India. It had 1.254 million employees as of March 2020, making it the world's eighth-largest employer.
    The government has committed to electrifying India's entire rail network by 2023-24, and become a "net zero (carbon emissions) railway" by 2030.</p>
